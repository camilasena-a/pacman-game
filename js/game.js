let createNewPacman = () => {
    pacman = new Pacman(
        oneBlockSize,
        oneBlockSize,
        oneBlockSize,
        oneBlockSize,
        oneBlockSize / 8
    );
};

let gameLoop = () => {
    // Check if the game is paused
    if (!window.gameIsPaused) {
        update();
    }
    draw();
    // Always make the requestAnimationFrame to keep the loop running
    requestAnimationFrame(gameLoop);
};

let restartPacmanAndGhosts = () => {
    createNewPacman();
    createGhosts();
};

let onGhostCollision = () => {
    lives--;
    
    // Add red border to the canvas
    const canvasElement = document.getElementById('canvas');
    canvasElement.classList.add('life-lost');
    
    // If there are still lives, remove the border after half a second
    if (lives > 0) {
        // Remove the border after half a second (animation duration)
        setTimeout(() => {
            canvasElement.classList.remove('life-lost');
        }, 500);
    }
    
    restartPacmanAndGhosts();
    if (lives == 0) {
        // Show the Game Over modal
        document.getElementById('gameover-modal').classList.remove('hidden');
        window.gameIsPaused = true;
        // Do not remove the red border when it's the last life
    }
};

let update = () => {
    pacman.moveProcess();
    pacman.eat();
    updateGhosts();
    if (pacman.checkGhostCollision(ghosts)) {
        onGhostCollision();
    }
    checkVictory();
};

let checkVictory = () => {
    // Check if all the balls have been eaten
    let foodCount = 0;
    for (let i = 0; i < map.length; i++) {
        for (let j = 0; j < map[0].length; j++) {
            if (map[i][j] == 2) {
                foodCount++;
            }
        }
    }
    
    // If there are no more balls, show the victory modal
    if (foodCount == 0) {
        // Pause the game
        window.gameIsPaused = true;
        
        // Update and show the modal with the final score
        document.getElementById('final-score').textContent = score;
        document.getElementById('victory-modal').classList.remove('hidden');
    }
};

let drawFoods = () => {
    for (let i = 0; i < map.length; i++) {
        for (let j = 0; j < map[0].length; j++) {
            if (map[i][j] == 2) {
                createRect(
                    j * oneBlockSize + oneBlockSize / 3,
                    i * oneBlockSize + oneBlockSize / 3,
                    oneBlockSize / 3,
                    oneBlockSize / 3,
                    "#FEB897"
                );
            }
        }
    }
};

let drawRemainingLives = () => {
    canvasContext.font = "16px Emulogic";
    canvasContext.fillStyle = "white";
    canvasContext.fillText("Lives: ", 180, oneBlockSize * (map.length + 2));

    for (let i = 0; i < lives; i++) {
        canvasContext.drawImage(
            pacmanFrames,
            2 * oneBlockSize,
            0,
            oneBlockSize,
            oneBlockSize,
            290 + i * oneBlockSize,
            oneBlockSize * (map.length + 2) - 15,
            oneBlockSize,
            oneBlockSize
        );
    }
};

let drawScore = () => {
    canvasContext.font = "16px Emulogic";
    canvasContext.fillStyle = "white";
    canvasContext.fillText(
        "Score: " + score,
        10,
        oneBlockSize * (map.length + 2)
    );
};

let draw = () => {
    canvasContext.clearRect(0, 0, canvas.width, canvas.height);
    createRect(0, 0, canvas.width, canvas.height, "black");
    drawWalls();
    drawFoods();
    
    // Draws ghosts and pacman only if the game is not paused
    if (!window.gameIsPaused) {
        drawGhosts();
        pacman.draw();
    }
    
    drawScore();
    drawRemainingLives();
};

let drawWalls = () => {
    for (let i = 0; i < map.length; i++) {
        for (let j = 0; j < map[0].length; j++) {
            if (map[i][j] == 1) {
                createRect(
                    j * oneBlockSize,
                    i * oneBlockSize,
                    oneBlockSize,
                    oneBlockSize,
                    "#342DCA"
                );
                if (j > 0 && map[i][j - 1] == 1) {
                    createRect(
                        j * oneBlockSize,
                        i * oneBlockSize + wallOffset,
                        wallSpaceWidth + wallOffset,
                        wallSpaceWidth,
                        wallInnerColor
                    );
                }

                if (j < map[0].length - 1 && map[i][j + 1] == 1) {
                    createRect(
                        j * oneBlockSize + wallOffset,
                        i * oneBlockSize + wallOffset,
                        wallSpaceWidth + wallOffset,
                        wallSpaceWidth,
                        wallInnerColor
                    );
                }

                if (i < map.length - 1 && map[i + 1][j] == 1) {
                    createRect(
                        j * oneBlockSize + wallOffset,
                        i * oneBlockSize + wallOffset,
                        wallSpaceWidth,
                        wallSpaceWidth + wallOffset,
                        wallInnerColor
                    );
                }

                if (i > 0 && map[i - 1][j] == 1) {
                    createRect(
                        j * oneBlockSize + wallOffset,
                        i * oneBlockSize,
                        wallSpaceWidth,
                        wallSpaceWidth + wallOffset,
                        wallInnerColor
                    );
                }
            }
        }
    }
};

let createGhosts = () => {
    ghosts = [];
    
    // Better distribute the ghosts so they can be applied in different positions
    const positionsX = [1, 9, 15, 7, 10, 18, 17, 17];
    const positionsY = [20, 17, 11, 4, 10, 19, 6, 21];
    
    for (let i = 0; i < ghostCount; i++) {
        let newGhost = new Ghost(
            positionsX[i] * oneBlockSize,
            positionsY[i] * oneBlockSize,
            oneBlockSize,
            oneBlockSize,
            (oneBlockSize / 16),
            ghostImageLocations[i % 4].x,
            ghostImageLocations[i % 4].y,
            124,
            116,
            10 + i
        );
        ghosts.push(newGhost);
    }
};

